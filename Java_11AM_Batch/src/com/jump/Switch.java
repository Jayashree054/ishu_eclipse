package com.jump;

public class Switch {

	public static void main(String[] args) {

		int month = 3;

		switch (month) {
		case 1:
			System.out.println("Condition 1 true");
			break;
		case 2:
			System.out.println("Condition 2 true");
			break;
		case 4:
			System.out.println("Condition 4 true");
			break;
		case 3:
			System.out.println("Condition 3 true");
			break;

		default:
			System.out.println();
			break;
		}

	}
}
