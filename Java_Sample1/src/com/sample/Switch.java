package com.sample;

public class Switch {
	public static void main(String[] args) {

		int month = 4;

		switch (month) {

		case 1:
			System.out.println("condition 1");
			break;
		case 4:
			System.out.println("condition 4");
			break;

		case 3:
			System.out.println("condition 3");
			break;

		case 2:
			System.out.println("condition 2");
			break;

		default:
			System.out.println("invalid");

			break;
		}

	}

}
