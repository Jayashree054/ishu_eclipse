package com.forloop;

public class While_Type1 {
	public static void main(String[] args) {

		int a = 10;

		while (a > 5) {
			System.out.println("true");
			a--;

		}
		System.out.println("*******************************");
		int s = 10;

		do {
			System.out.println(s);
			s--;
		} while (s < 5);
	}

}
